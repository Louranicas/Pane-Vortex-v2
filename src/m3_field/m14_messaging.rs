/// Phase-modulated inter-sphere message types.
///
/// Messages are generated by the field decision engine, conductor, or explicit
/// sphere-to-sphere communication. Each variant may or may not have a phase effect.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum PhaseMessage {
    /// Steer two spheres' phases closer together (attention alignment).
    AttentionAlignment {
        /// Sender sphere.
        from: PaneId,
        /// Target sphere.
        to: PaneId,
        /// Reason for alignment.
        context: String,
    },

    /// Request chimera formation (phase separation) between two spheres.
    ChimeraRequest {
        /// Sender sphere.
        from: PaneId,
        /// Target sphere.
        to: PaneId,
        /// Reason for chimera request.
        reason: String,
    },

    /// Shared write notification (consensus coordination).
    ConsensusWrite {
        /// Sender sphere.
        from: PaneId,
        /// Target sphere.
        to: PaneId,
        /// Payload (e.g. file path, task description).
        payload: String,
    },

    /// Emergency coherence: pull multiple targets toward sender's phase.
    EmergencyCoherence {
        /// Sender sphere initiating emergency.
        from: PaneId,
        /// Target spheres (capped at 50 per C12).
        targets: Vec<PaneId>,
        /// Reason for emergency.
        reason: String,
    },

    /// Resonance match: two spheres discovered overlapping buoys (tunnel).
    ResonanceMatch {
        /// Sender sphere.
        from: PaneId,
        /// Target sphere.
        to: PaneId,
        /// Label of the matching buoy.
        buoy_label: String,
    },
}

/// Maximum targets in an `EmergencyCoherence` message (C12: bounded collections).
#[allow(dead_code)] // Used transitively in truncate_fields (pub(crate), not yet wired to callers)
const EMERGENCY_TARGETS_MAX: usize = 50;

/// Maximum string field length for truncation.
#[allow(dead_code)] // Used transitively in truncate_fields (pub(crate), not yet wired to callers)
const STRING_FIELD_MAX: usize = 256;

impl PhaseMessage {
    /// Whether this message has a phase effect when applied.
    #[must_use]
    #[allow(dead_code)] // Designed API; callers will be added in L7 when field messaging is wired
    pub(crate) const fn has_phase_effect(&self) -> bool {
        matches!(
            self,
            Self::AttentionAlignment { .. }
                | Self::EmergencyCoherence { .. }
                | Self::ResonanceMatch { .. }
        )
    }

    /// Truncate all string fields to prevent oversized messages.
    #[allow(dead_code)] // Input sanitization API; wired to message ingestion in L7
    pub(crate) fn truncate_fields(&mut self) {
        match self {
            Self::AttentionAlignment { context, .. } => {
                *context = truncate_string(context, STRING_FIELD_MAX);
            }
            Self::ChimeraRequest { reason, .. } => {
                *reason = truncate_string(reason, STRING_FIELD_MAX);
            }
            Self::ConsensusWrite { payload, .. } => {
                *payload = truncate_string(payload, STRING_FIELD_MAX);
            }
            Self::EmergencyCoherence {
                targets, reason, ..
            } => {
                *reason = truncate_string(reason, STRING_FIELD_MAX);
                targets.truncate(EMERGENCY_TARGETS_MAX);
            }
            Self::ResonanceMatch { buoy_label, .. } => {
                *buoy_label = truncate_string(buoy_label, STRING_FIELD_MAX);
            }
        }
    }

    /// The sender sphere ID.
    #[must_use]
    #[allow(dead_code)] // Used in message routing (L7 m34_suggestions wires this in a future pass)
    pub(crate) const fn sender_id(&self) -> &PaneId {
        match self {
            Self::AttentionAlignment { from, .. }
            | Self::ChimeraRequest { from, .. }
            | Self::ConsensusWrite { from, .. }
            | Self::EmergencyCoherence { from, .. }
            | Self::ResonanceMatch { from, .. } => from,
        }
    }

    /// The primary target sphere ID (if applicable).
    ///
    /// Returns `None` for `EmergencyCoherence` (which has multiple targets).
    #[must_use]
    #[allow(dead_code)] // Part of message routing API; callers added in future L7 pass
    pub(crate) const fn target_id(&self) -> Option<&PaneId> {
        match self {
            Self::AttentionAlignment { to, .. }
            | Self::ChimeraRequest { to, .. }
            | Self::ConsensusWrite { to, .. }
            | Self::ResonanceMatch { to, .. } => Some(to),
            Self::EmergencyCoherence { .. } => None,
        }
    }

    /// Compute the phase adjustment this message would apply to a target sphere.
    ///
    /// Returns `(target_id, phase_delta)` pairs. `phase_delta` is in radians,
    /// already modulated by message-specific strength.
    #[must_use]
    #[allow(dead_code)] // Phase computation API for future L4 coupling integration
    pub(crate) fn phase_adjustments(
        &self,
        sender_phase: f64,
        target_phases: &std::collections::HashMap<PaneId, f64>,
    ) -> Vec<(PaneId, f64)> {
        match self {
            Self::AttentionAlignment { to, .. } => {
                if let Some(&target_phase) = target_phases.get(to) {
                    let delta = phase_diff(sender_phase, target_phase);
                    // Gentle steering: 5% of the phase difference
                    vec![(to.clone(), delta * 0.05)]
                } else {
                    Vec::new()
                }
            }
            Self::EmergencyCoherence { targets, .. } => targets
                .iter()
                .filter_map(|t| {
                    target_phases.get(t).map(|&target_phase| {
                        let delta = phase_diff(sender_phase, target_phase);
                        // Stronger pull: 15% of the phase difference
                        (t.clone(), delta * 0.15)
                    })
                })
                .collect(),
            Self::ResonanceMatch { to, .. } => {
                if let Some(&target_phase) = target_phases.get(to) {
                    let delta = phase_diff(sender_phase, target_phase);
                    // Moderate alignment: 10% of the phase difference
                    vec![(to.clone(), delta * 0.10)]
                } else {
                    Vec::new()
                }
            }
            // No phase effect
            Self::ChimeraRequest { .. } | Self::ConsensusWrite { .. } => Vec::new(),
        }
    }
}

