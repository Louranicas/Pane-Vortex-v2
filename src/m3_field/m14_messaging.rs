//! # M14: Phase Messaging
//!
//! Five `PhaseMessage` types for inter-sphere communication.
//! Messages can influence phase, coupling, or coordination.
//!
//! ## Layer: L3 (Field)
//! ## Module: M14
//! ## Dependencies: L1 (M01 types, M06 validation)

