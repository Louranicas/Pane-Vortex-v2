    // ── phase_adjustments ──

    #[test]
    fn phase_adjustment_attention_alignment() {
        let msg = PhaseMessage::AttentionAlignment {
            from: pid("a"),
            to: pid("b"),
            context: "test".into(),
        };
        let mut target_phases = HashMap::new();
        target_phases.insert(pid("b"), 1.0);

        let adjustments = msg.phase_adjustments(0.0, &target_phases);
        assert_eq!(adjustments.len(), 1);
        assert_eq!(adjustments[0].0.as_str(), "b");
        // Sender at 0.0, target at 1.0 → delta = -1.0 → adjustment = -0.05
        assert_relative_eq!(adjustments[0].1, -0.05, epsilon = 1e-10);
    }

